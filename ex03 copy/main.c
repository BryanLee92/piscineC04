#include <stdio.h>

int ft_atoi(char *str);

int main()
{
	// 123123
	printf("%i\n", ft_atoi("	  --123123"));
	// -872634
	printf("%i\n", ft_atoi("\t\n\v\f\r  	---872634"));
	// -42
	printf("%i\n", ft_atoi("\t\v\f	    	-42banana"));
	// 0 
	printf("%i\n", ft_atoi(""));
	// -1234
	printf("%i\n", ft_atoi(" ---+--+1234ab567"));
	// 123
	printf("%i\n", ft_atoi("123"));
	// -456
	printf("%i\n", ft_atoi("-456"));
}
